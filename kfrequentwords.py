from collections import Counter
from heapq import heappush, heappop
from functools import cmp_to_key
class Solution:
    def topKFrequent(self, words, k):
        """
        :type words: List[str]
        :type k: int
        :rtype: List[str]
        """
        def compate(item1, item2):
            if item1[0] != item2[0]:
                return item1[0] - item2[0]
            else:
                if item1[1] < item2[1]:
                    return 1
                elif item2[1] < item1[1]:
                    return -1
                return 0
        ctr = Counter()
        # print(ctr)
        for word in words:
            ctr[word] += 1
        ans = []
        for key in ctr.keys():
            ans.append((ctr[key], key))
        ans = sorted(ans, key=cmp_to_key(compate), reverse=True)
        result = []
        for i in range(0, min(len(ans), k)):
            result.append(ans[i][1])
        return result

